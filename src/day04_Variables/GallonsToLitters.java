package day04_Variables;

public class GallonsToLitters {

    public static void main(String[] args) {

        // 1 gallon = 3.79 lt

        double gallons = 5;
        double litters = gallons * 3.79;
        System.out.println();
        System.out.println("gallons = " + gallons);
        System.out.println("litters = " + litters);

    }

}
