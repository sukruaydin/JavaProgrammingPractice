package day04_Variables;

public class KgToPounds {

    public static void main(String[] args) {

        // 1 kg = 2.20462262 lb

        double kg = 4;
        double lb = kg * 2.20462262;

        System.out.println();
        System.out.println("kg = " + kg);
        System.out.println("lb = " + lb);

    }

}
