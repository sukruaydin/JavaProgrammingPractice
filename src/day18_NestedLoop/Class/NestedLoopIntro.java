package day18_NestedLoop.Class;

public class NestedLoopIntro {

    public static void main(String[] args) {
        //print 1~5, 5 times
        for (int j = 1; j <= 5 ; j++) {

            for (int i = 1; i <= 5 ; i++) {
                System.out.print(i+" ");

        }
            System.out.println();
      }
    }
}
