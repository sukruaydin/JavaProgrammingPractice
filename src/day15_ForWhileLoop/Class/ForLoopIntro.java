package day15_ForWhileLoop.Class;

public class ForLoopIntro {

    public static void main(String[] args) {
        //for block ----> for(initialization, condition, iteration) {statement}
        //order ---> 1.initialization, 2.condition, 3.statement, 4.iterator
        for(int i = 1; i <=10; i++){
            System.out.println("Hello World");
        }

    }

}
