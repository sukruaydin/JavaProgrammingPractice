package day05_StringConcatenations;

public class CarInfo {

    public static void main(String[] args) {

        int year = 2015,
                km = 150_000,
                price = 525_000;
        String make = "Audi",
                model = "A3",
                color = "Black";

        System.out.println(year + " " + make + " " + model + ", " + km + " km, " + color + ", $" + price + "." );




    }

}
