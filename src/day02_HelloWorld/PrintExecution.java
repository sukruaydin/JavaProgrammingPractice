package day02_HelloWorld;

public class PrintExecution {

    public static void main(String[] args) {

        //appends a new line after execution
        System.out.println("Ömer Engin");
        System.out.println("Yusuf Taş");

        //does not append a new line after execution
        System.out.println();
        System.out.println("Abdülfındık Samet");
        System.out.println();

    }

}
