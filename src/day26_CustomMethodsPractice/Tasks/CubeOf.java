package day26_CustomMethodsPractice.Tasks;

public class CubeOf {

    public static void main(String[] args) {

        System.out.println(cubeOf(3));

    }

    //results the cube of given int, returns int
    public static int cubeOf(int num){

        return num*num*num;
    }
    //results the cube of given double, returns double
    public static double cubeOf(double num){

        return num*num*num;
    }


}
