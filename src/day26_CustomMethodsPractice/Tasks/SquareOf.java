package day26_CustomMethodsPractice.Tasks;

public class SquareOf {

    public static void main(String[] args) {

        System.out.println(squareOf(5));

    }

    //results the square of given int, returns int
    public static int squareOf(int num){

        return num*num;
    }
    //results the square of given double, returns double
    public static double squareOf(double num){

        return num*num;
    }


}
